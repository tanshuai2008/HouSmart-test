def get_state_benchmarks(state_name):
    """
    Returns a dictionary of benchmark data for a given state name and the national average.
    """
    state_name = state_name.strip()
    
    # Handle DC edge case 
    if "District" in state_name and "Columbia" in state_name:
        state_name = "District of Columbia"
    
    # Defaults
    s_income = INCOME_DATA.get(state_name, None)
    s_edu = EDUCATION_DATA.get(state_name, None)
    s_age = AGE_DATA.get(state_name, None)
    s_race = RACE_DATA.get(state_name, None)
    
    us_income = INCOME_DATA.get("United States")
    us_edu = EDUCATION_DATA.get("United States")
    us_age = AGE_DATA.get("United States")
    us_race = RACE_DATA.get("United States")
    
    return {
        "state_name": state_name,
        "state_income": s_income,
        "state_edu": s_edu, # [HighSchool+, Bachelors+, Advanced+]
        "state_age": s_age, # [Under 18, 18-24, 25-44, 45-64, Above 64]
        "state_race": s_race, # [White, Hispanic, Black, Asian, Other]
        "us_income": us_income,
        "us_edu": us_edu,
        "us_age": us_age,
        "us_race": us_race
    }
